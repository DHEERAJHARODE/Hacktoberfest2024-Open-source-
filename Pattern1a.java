class Pattern1a
{
    public static void main(String...a1)
    {
        int m=4;
        int n=5;
        for(int i=0;i<m;i++)
        {
            for(int j=0;j<n;j++)
            {
                System.out.print('*');
            }
            System.out.println();
        }
    }
}